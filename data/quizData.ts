export function getDungeon(id: number): Dungeon | undefined {
  return dungeons.find(d => d.id === id)
}

export function getStage(stageId: string): { dungeon: Dungeon; stage: Stage } | undefined {
  for (const dungeon of dungeons) {
    const stage = dungeon.stages.find(s => s.id === stageId)
    if (stage) return { dungeon, stage }
  }
  return undefined
}